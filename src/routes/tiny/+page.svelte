<canvas id="c"></canvas>
<script>
    "use strict";
    const canvas = document.getElementById('c');
    const glsl = SwissGL(canvas);
    glsl.loop(({time})=>{
        glsl.adjustCanvas();        
        glsl({time, Aspect:'cover',FP:`
            sin(length(XY)*vec3(30,30.5,31)
            -time+atan(XY.x,XY.y)*3.),1`});
    });
</script>
